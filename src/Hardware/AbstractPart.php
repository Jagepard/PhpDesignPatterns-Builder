namespace Creational\Builder\Hardware;

abstract class AbstractPart
{
    /**
     * @var string
     */
    protected $value;

    /**
     * AbstractParameter constructor.
     * @param  string  $value
     */
    public function __construct(string $value)
    {
        $this->value = $value;
    }

    /**
     * @return string
     */
    public function getValue(): string
    {
        return $this->value;
    }
}
